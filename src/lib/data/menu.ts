export function getAllergenTags(description: string): string[] {
  const tags: string[] = [];
  const lower = description.toLowerCase();
  if (lower.includes('gluten free')) tags.push('GF');
  if (lower.includes('dairy free')) tags.push('DF');
  if (lower.includes('vegan')) tags.push('V');
  return tags;
}
